package com.three;

public class Person 
{
private int id;
private String name;
private long phno;
private Mobile mobile;


public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public long getPhno() {
	return phno;
}
public void setPhno(long phno) {
	this.phno = phno;
}
public Mobile getMobile() {
	return mobile;
}
public void setMobile(Mobile mobile) {
	this.mobile = mobile;
}


}
